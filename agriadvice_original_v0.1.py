# ==========================================
# 1. Initialize the Acoustic Synthesizer
# ==========================================
# We load a lightweight TTS model into the remaining GPU memory.
# (Using the highly stable mini-v1 which supports conditional voice prompting)
tts_model_name = "parler-tts/parler-tts-mini-v1"
tts_tokenizer = AutoTokenizer.from_pretrained(tts_model_name)
tts_model = ParlerTTSForConditionalGeneration.from_pretrained(tts_model_name).to("cuda")

# ==========================================
# 2. Define the Voicebox Node
# ==========================================
def voicebox_node(state):
    # Extract the finalized, policy-cleared text
    text_to_speak = state["final_safe_response"]

    # We define the acoustic style (this can be dynamically altered later)
    voice_description = "A clear, professional female voice speaking at a moderate pace with a neutral tone."

    # Tokenize both the style prompt and the actual text
    input_ids = tts_tokenizer(voice_description, return_tensors="pt").input_ids.to("cuda")
    prompt_input_ids = tts_tokenizer(text_to_speak, return_tensors="pt").input_ids.to("cuda")

    # Generate the raw audio waveform
    generation = tts_model.generate(input_ids=input_ids, prompt_input_ids=prompt_input_ids)
    audio_waveform = generation.cpu().numpy().squeeze()

    # Save the waveform to the Colab hard drive as a .wav file
    output_filename = "ai_final_response.wav"
    sf.write(output_filename, audio_waveform, tts_model.config.sampling_rate)

    # Mutate the state with the final physical file path
    state["output_audio_path"] = output_filename

    return state

