reasoning_pipeline = pipeline(
    "text-generation",
    model=model,
    tokenizer=tokenizer,
    max_new_tokens=256,  # Hard cap for prototype speed
    temperature=0.3,     # Low temperature forces logical, factual responses over creative ones
)

# 5. Define the actual Node Function that our LangGraph Manager will call
def brain_node(state):
    # Extract the necessary variables from the moving state dictionary
    user_text = state["user_transcript"]
    lang = state["detected_language"]

    # We use a placeholder for context right now until we build the RAG (Retrieval) Node
    context = state.get("retrieved_context", "No background context provided yet.")

    # Dynamically inject the detected language into the system prompt to force the output language
    system_prompt = f"""You are an intelligent reasoning agent.
    The user spoke in language code: '{lang}'.
    Context: {context}

    Answer the user's query using the context.
    CRITICAL: Your final output MUST be written in the exact same language as the user's language code ({lang})."""

    # Format the messages in the standard chat template
    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_text}
    ]

    # Generate the reasoning and extract the raw text
    outputs = reasoning_pipeline(messages)
    generated_text = outputs[0]["generated_text"][-1]["content"]

    # Update the state clipboard with the drafted response
    state["draft_response"] = generated_text

    return state

