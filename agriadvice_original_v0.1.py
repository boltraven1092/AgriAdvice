# 2. Define the Guardrail Node
# ==========================================
def guardrail_node(state):
    # Retrieve the raw text drafted by the Brain
    drafted_text = state["draft_response"]

    # Define the strict boundaries of our system
    policy_labels = ["safe and helpful", "toxic, hateful, or harmful advice"]

    # Execute the probability matrix scan
    classification = policy_classifier(drafted_text, policy_labels)

    # Extract the label with the highest mathematical confidence
    top_label = classification['labels'][0]

    # The Routing Logic:
    if top_label == "toxic, hateful, or harmful advice":
        # Overwrite the drafted response with a hardcoded safety fallback
        state["final_safe_response"] = "Policy Override: I cannot fulfill this request safely."
        state["error_flag"] = "Safety Violation Triggered"
    else:
        # If safe, promote the draft to the final output pipeline
        state["final_safe_response"] = drafted_text

    return state

