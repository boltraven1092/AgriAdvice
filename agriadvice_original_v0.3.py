# ==========================================
# Squad 1, Agent 1: The Perception Agent
# ==========================================
def perception_node(state: SwarmState):
    audio_path = state["user_audio_path"]

    # 1. Acoustic Ingestion & Transcription
    segments, info = audio_model.transcribe(audio_path, beam_size=5)
    native_text = " ".join([segment.text for segment in segments])

    # 2. The Hindustani Acoustic Patch
    detected_lang = info.language
    if detected_lang == "ur":
        detected_lang = "hi"

    # 3. Language Decoupling (Translate to English)
    if detected_lang != "en":
        english_text = GoogleTranslator(source=detected_lang, target='en').translate(native_text)
    else:
        english_text = native_text

    # Update the Swarm Clipboard
    state["detected_language"] = detected_lang
    state["native_transcript"] = native_text
    state["english_query"] = english_text

    return state

