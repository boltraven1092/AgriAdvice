# Harvester 1: The Meteo-Agent (Fully Functional Open-Source)
async def fetch_weather(session, location_name):
    try:
        # Step 1: Geocoding (Text to Coordinates)
        geo_url = f"https://geocoding-api.open-meteo.com/v1/search?name={location_name}&count=1"
        async with session.get(geo_url, timeout=5) as geo_res:
            geo_data = await geo_res.json()

            if not geo_data.get("results"):
                return {"weather": f"Could not find coordinates for {location_name}."}

            lat = geo_data["results"][0]["latitude"]
            lon = geo_data["results"][0]["longitude"]

        # Step 2: Hyper-local Weather Fetch
        weather_url = f"https://api.open-meteo.com/v1/forecast?latitude={lat}&longitude={lon}&current_weather=true"
        async with session.get(weather_url, timeout=5) as weather_res:
            w_data = await weather_res.json()
            temp = w_data["current_weather"]["temperature"]
            wind = w_data["current_weather"]["windspeed"]
            return {"weather": f"Temp: {temp}°C, Wind: {wind} km/h at {lat},{lon}"}

    except Exception as e:
        return {"weather": f"Meteo-Agent Offline: {str(e)}"}

