# Squad 1, Agent 2: The Semantic Gatekeeper
# ==========================================
def gatekeeper_node(state: SwarmState):
    # The Gatekeeper only evaluates the clean English query
    query = state["english_query"]

    # Strict boundary definitions
    policy_labels = [
        "agricultural question about farming, crops, weather, or soil",
        "profanity, toxic, or offensive language",
        "general chat, technology, movies, or out of domain topics"
    ]

    # Microscopic probability scan (Takes ~50 milliseconds)
    classification = policy_classifier(query, policy_labels)
    top_label = classification['labels'][0]

    lang = state["detected_language"]

    # ---------------------------------------------------------
    # The Short-Circuit Routing Logic
    # ---------------------------------------------------------
    if top_label == "profanity, toxic, or offensive language":
        state["routing_error"] = "PROFANITY_DETECTED"
        rejection_en = "Please use respectful language. I am an agricultural assistant."
        # Translate rejection back to native dialect
        state["final_translated_text"] = GoogleTranslator(source='en', target=lang).translate(rejection_en)

    elif top_label == "general chat, technology, movies, or out of domain topics":
        state["routing_error"] = "OUT_OF_DOMAIN"
        rejection_en = "I am an agricultural expert. Please ask questions related to farming, crops, or soil."
        # Translate rejection back to native dialect
        state["final_translated_text"] = GoogleTranslator(source='en', target=lang).translate(rejection_en)

    else:
        # Query is safe. Green light for the rest of the Swarm.
        state["routing_error"] = None

    return state

